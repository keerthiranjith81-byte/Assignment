// push, pop, shift, unshift.

let animals=["Dog","Tiger","Lion"];
console.log(animals);


// add elements to the start
animals.unshift("Cat");
console.log("After unshift:",animals);   

// add elements to the end
animals.push("Cow");
console.log("After push:",animals);

// remove elements from the start
animals.shift();
console.log("After shift:",animals);

// remove elements from the end
animals.pop();
console.log("After pop:",animals);




